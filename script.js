function showVideoSchedule() {
    const videoScheduleDiv = document.getElementById('videoSchedule');
    const currentDateDiv = document.getElementById('currentDate');
    
    // Get the current date in the local timezone and format it as YYYY-MM-DD
    const currentDate = new Date().toLocaleDateString('en-CA'); // 'en-CA' gives the format YYYY-MM-DD

    currentDateDiv.innerText = `Today is ${currentDate}`;

    if (videoScheduleData[currentDate]) {
        videoScheduleDiv.innerText = videoScheduleData[currentDate];
    } else {
        videoScheduleDiv.innerText = 'No videos scheduled for today.';
    }
}

